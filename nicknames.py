class Nicknames:

    def getNickname(userString):
        m = hashlib.sha256()
        m.update(userString.encode())
        hash = m.digest()
        idx = int.from_bytes(hash, byteorder='big', signed=False)
        idx = idx % len(NICKS)
        return NICKS[idx]

